package com.example.budgetbuddy1.database

import androidx.room.Entity
import androidx.room.PrimaryKey

@Entity(tableName = "users")
data class User(
    @PrimaryKey(autoGenerate = true)
    val id: Int = 0,
    val username: String,
    val email: String,
    val phoneNumber: String = "", // NEW field for profile
    val password: String
)